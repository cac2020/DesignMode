package mode.行为.命令;

public class TV {
    public void on() {
        System.out.println(" 电视机开启");
    }

    public void off() {
        System.out.println(" 电视机关闭");
    }

    public void channelUp() {
        System.out.println(" 电视机频道+");
    }

    public void channelDown() {
        System.out.println(" 电视机频道-");
    }

    public void volumeUp() {
        System.out.println(" 电视机音量+");
    }

    public void volumeDown() {
        System.out.println(" 电视机音量-");
    }

}
